var baseurl;
function getBaseURL() {
    if (!baseurl) {
        var r = new RegExp("(^|(.*?\\/))(SuperMap.Include\.js)(\\?|$)"),
            s = document.getElementsByTagName('script'),
            src, m;
        for (var i = 0, len = s.length; i < len; i++) {
            src = s[i].getAttribute('src');
            if (src) {
                var m = src.match(r);
                if (m) {
                    baseurl = m[1];
                    break;
                }
            }
        }
    }
    return baseurl;
};
function inputScript(inc) {
    var script = '<' + 'script type="text/javascript" src="' + getBaseURL() + 'Plugins/Plotting/' + inc + '"'+ '><' + '/script>';
    document.writeln(script);
}